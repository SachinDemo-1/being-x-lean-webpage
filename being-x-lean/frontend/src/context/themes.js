export const applyTheme = (themeName) => {
  const theme = THEMES[themeName] || THEMES['dark-fire'];
  const root = document.documentElement;
  Object.entries(theme).forEach(([key, value]) => {
    if (key !== 'label') root.style.setProperty(key, value);
  });
};
